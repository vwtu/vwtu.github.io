function openMore() {
	$(".expanded").css("display", "inline-block");
	$(".third-section-more").css("display", "none");
	$(".hr-more").css("display", "block");
}

function closeMore() {
	$(".expanded").css("display", "none");
	$(".third-section-more").css("display", "inline-block");
	$(".hr-more").css("display", "none");
}